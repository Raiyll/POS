    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stat = conn.prepareStatement(
            "update barang set nama_barang=?, harga=?, satuan =?, stok =? where kode_barang =?");
            stat.setString(1, jTextField_nama_barang.getText());
            stat.setString(2, jTextField_harga.getText());
            stat.setString(3, jComboBox_satuan.getSelectedItem().toString());
            stat.setString(4, jTextField_stok.getText());
            stat.setString(5, jTextField_kode_barang.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diedit", "pesan",
                    JOptionPane.INFORMATION_MESSAGE);
            hapusData();
            TampilData();
            this.awal();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data tidak berhasil disimpan" + e, "pesan",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton_editActionPerformed

