    private void jButton_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cariActionPerformed
        // TODO add your handling code here:
        Connection conn = Koneksi.getConnection();
        try {
            PreparedStatement stat = conn.prepareStatement("select * from barang where kode_barang = ?");
            stat.setString(1, jTextField_cari.getText());
            ResultSet resultSet = stat.executeQuery();
            if (resultSet.next()) {
                jTextField_kode_barang.setText(resultSet.getString("kode_barang"));
                jTextField_nama_barang.setText(resultSet.getString("nama_barang"));
                
                for (int i = 0; i < jComboBox_satuan.getItemCount(); i++) {
                    if (jComboBox_satuan.getItemAt(i).equals(resultSet.getString("satuan"))) {
                        jComboBox_satuan.setSelectedIndex(1);
                    }
                }
                
                jTextField_harga.setText(resultSet.getString("harga"));
                jTextField_stok.setText(resultSet.getString("stok"));
                jButton_edit.setEnabled(true);
                this.aktif();
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton_cariActionPerformed

    private void jTable_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_barangMouseClicked
        // TODO add your handling code here:
         int baris = jTable_barang.getSelectedRow();

    jTextField_kode_barang.setText(
        DftTblModel_barang.getValueAt(baris, 0).toString()
    );
    jTextField_nama_barang.setText(
        DftTblModel_barang.getValueAt(baris, 1).toString()
    );
    jTextField_harga.setText(
        DftTblModel_barang.getValueAt(baris, 2).toString()
    );

    for (int i = 0; i < jComboBox_satuan.getItemCount(); i++) {
        if (jComboBox_satuan.getItemAt(i)
            .equals(DftTblModel_barang.getValueAt(baris, 3).toString())) {
            jComboBox_satuan.setSelectedIndex(i);
        }
    }

    jTextField_stok.setText(
        DftTblModel_barang.getValueAt(baris, 4).toString()
    );

    aktif(); // biar langsung bisa diedit
    }//GEN-LAST:event_jTable_barangMouseClicked

